    public void Salvar_Arq(String filename, byte[] array) {
        try {
            FileOutputStream fout = new FileOutputStream(D + "/" + filename);
            //Write the file contents to the new file created
            fout.write(array);
            fout.close();
            System.out.println("Arquivo foi baixado com sucesso!");
            JOptionPane.showMessageDialog(null, "Arquivo foi baixado com sucesso!");
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    int sc_id(String nome) {
        for (int i = 0; i < a.length; i++) {
            if (nome == a[i]) {
                return i;
            }
        }

        return -1;

    }

    String cs_arq(String aquiv) {
        conexoes = SA.qtd_clientes();
        String[] clients = SA.att_clientes();
        for (int i = 0; i < conexoes; i++) {
            String[] aux = SA.get_Arq(i);
            for (int j = 0; j < aux.length; j++) {
                //aux[j].toLowerCase().contains(aquiv.toLowerCase())
                if (aux[j].toLowerCase().contains(aquiv.toLowerCase())) {
                    return clients[i];
                }
            }
        }
        return null;

    }

